            let navbarHeight = navbar ? navbar.offsetHeight : 50; // Get navbar height
            let extraSpacing = 10; // Additional space for better visibility

            window.scrollTo({
               top: element.offsetTop - navbarHeight - extraSpacing,
               behavior: 'smooth'
            });
         }
      };

